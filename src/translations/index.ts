export const getTranslation = (currentLanguage: string, key: string, params?: { [key: string]: string | number }) => {
    const lang = languages.find(l => l.name === currentLanguage);
    const code = lang ? lang.code : 'en';
    let text = translations[code]?.[key] || translations.en?.[key] || key;

    if (params) {
        Object.keys(params).forEach(param => {
            text = text.replace(new RegExp(`{{${param}}}`, 'g'), String(params[param]));
        });
    }

    return text;
};
